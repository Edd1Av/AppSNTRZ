class Farmacovigilancia {
  String centro = "";
  String especie = "";
  String farmaco = "";
  String inicio = "";
  String fin = "";

  Farmacovigilancia(String centro, String especie, String farmaco,
      String inicio, String fin) {
    this.centro = centro;
    this.especie = especie;
    this.farmaco = farmaco;
    this.inicio = inicio;
    this.fin = fin;
  }
}
