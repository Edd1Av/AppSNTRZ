class Siembra {
  String fecha = "";
  String centroAcuicola = "";
  String especie = "";
  double cantidadPL = 0;

  Siembra(
      String fecha, String centroAcuicola, String especie, double cantidadPL) {
    this.fecha = fecha;
    this.centroAcuicola = centroAcuicola;
    this.especie = especie;
    this.cantidadPL = cantidadPL;
  }
}
