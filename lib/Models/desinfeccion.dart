class Desinfeccion {
  String centro = "";
  String especie = "";
  String proveedor = "";
  String farmaco = "";
  String desinfeccion = "";

  Desinfeccion(String centro, String especie, String proveedor, String farmaco,
      String desinfeccion) {
    this.centro = centro;
    this.especie = especie;
    this.proveedor = proveedor;
    this.farmaco = farmaco;
    this.desinfeccion = desinfeccion;
  }
}
